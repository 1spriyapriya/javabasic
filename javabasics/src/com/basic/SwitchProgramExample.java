package com.basic;

public class SwitchProgramExample {
	public static void main(String[]args)
	{
		int choice=20;
		switch(choice)
		{
		case 10:
			System.out.println("10");
		break;
		case 20:
			System.out.println("20");
			break;
			default:
				System.out.println("default");
				break;
		}
	}

}
